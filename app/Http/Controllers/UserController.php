    {
        if ($request->ajax()) {
            $usuarios = User::oldest()->get();

            return DataTables::of($usuarios)
                            ->setRowAttr(['data-id' => function($usuarios) {
                                return $usuarios->id;
                            }])
                            ->addIndexColumn()
                            ->make(true);
        }

        return view('usuarios.index');
    }
    
